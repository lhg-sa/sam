    if not frappe.db.exists("DocType", doctype_name):
        return

    table = f"`tab{doctype_name}`"
    cases = " ".join(
        f"WHEN {key} THEN '{value}' WHEN '{key}' THEN '{value}'"
        for key, value in TIPO_MARCA_MAP.items()
    )
    sql = f"""
        UPDATE {table}
        SET idmarca = CASE idmarca
            {cases}
            ELSE ''
        END
    """
    frappe.db.sql(sql)
    frappe.db.commit()
